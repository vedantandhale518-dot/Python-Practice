name = "Vedant"

print(name[0:3])

print(name[-4:-1])   # If we are counting from front then it starts from 0
print(name[2:5])     # If we are counting from back then it starts from -1

print(name[:5])    # Same as print(name[0:5])
print(name[1:])    # Same as print(name[1:6])
print(name[1:6])